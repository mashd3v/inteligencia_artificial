# Variables
''' 
    Que es una variable?
    Una variable es una "caja" o lugar en donde puedo almacenar objetos, numeros, texto, etc.

    Reglas para escribir una variable en python:
        - No puede comenzar con numeros
        - Debe ser escrita en minusculas
        - Para separar palabras se utilizan guiones bajos
'''
# Ejemplos:
# integers
primer_numero = 50
segundo_numero = 30
print('Ejemplos de numeros enteros')
print(f'Primer numero: {primer_numero}')
print(f'Primer numero: {segundo_numero}')

# doubles
tercer_numero = 50.52
cuarto_numero = 30.32
print('\nEjemplos de numeros decimales o dobles')
print(f'Primer numero: {tercer_numero}')
print(f'Primer numero: {cuarto_numero}')

# strings
nombre = 'Miguel'
apellido = 'Soto'
print('\nEjemplos de cadenas de texto')
print(f'Nombre y apellido: {nombre} {apellido}')
print(nombre * 4)

# booleans
es_estudiante = True
trabaja = False
print('\nEjemplos de booleanos')
print(f'Estudia: {es_estudiante}')
print(f'Trabaja: {trabaja}')
